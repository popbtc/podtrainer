import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

/// A class that contains all theme configurations for the sports training application.
/// Implements "Purposeful Athletic Minimalism" design philosophy with Performance Blue Foundation.
class AppTheme {
  AppTheme._();

  // Performance Blue Foundation Color System
  static const Color primaryLight = Color(0xFF2196F3); // Core brand blue
  static const Color primaryVariantLight =
      Color(0xFF1976D2); // Darker blue variant
  static const Color secondaryLight =
      Color(0xFF4CAF50); // Success/connected status
  static const Color secondaryVariantLight =
      Color(0xFF388E3C); // Darker success variant
  static const Color backgroundLight = Color(0xFFFAFAFA); // Subtle off-white
  static const Color surfaceLight = Color(0xFFFFFFFF); // Pure white for cards
  static const Color errorLight = Color(0xFFF44336); // Disconnection alerts
  static const Color warningLight =
      Color(0xFFFF9800); // Low battery/connection issues
  static const Color accentLight =
      Color(0xFFE3F2FD); // Light blue tint for highlights
  static const Color onPrimaryLight = Color(0xFFFFFFFF);
  static const Color onSecondaryLight = Color(0xFFFFFFFF);
  static const Color onBackgroundLight =
      Color(0xFF212121); // High contrast dark gray
  static const Color onSurfaceLight = Color(0xFF212121);
  static const Color onErrorLight = Color(0xFFFFFFFF);

  // Dark theme colors maintaining athletic minimalism
  static const Color primaryDark =
      Color(0xFF64B5F6); // Lighter blue for dark mode
  static const Color primaryVariantDark =
      Color(0xFF42A5F5); // Variant for dark mode
  static const Color secondaryDark = Color(0xFF81C784); // Success in dark mode
  static const Color secondaryVariantDark =
      Color(0xFF66BB6A); // Darker success variant
  static const Color backgroundDark =
      Color(0xFF121212); // Material dark background
  static const Color surfaceDark =
      Color(0xFF1E1E1E); // Elevated surface in dark
  static const Color errorDark = Color(0xFFEF5350); // Error in dark mode
  static const Color warningDark = Color(0xFFFFB74D); // Warning in dark mode
  static const Color accentDark = Color(0xFF1A237E); // Dark blue accent
  static const Color onPrimaryDark = Color(0xFF000000);
  static const Color onSecondaryDark = Color(0xFF000000);
  static const Color onBackgroundDark = Color(0xFFFFFFFF);
  static const Color onSurfaceDark = Color(0xFFFFFFFF);
  static const Color onErrorDark = Color(0xFF000000);

  // Card and dialog colors for clean separation
  static const Color cardLight = Color(0xFFFFFFFF);
  static const Color cardDark = Color(0xFF2D2D2D);
  static const Color dialogLight = Color(0xFFFFFFFF);
  static const Color dialogDark = Color(0xFF2D2D2D);

  // Minimal shadow colors for subtle depth
  static const Color shadowLight = Color(0x1A000000); // 0.1 opacity
  static const Color shadowDark = Color(0x33FFFFFF); // 0.2 opacity

  // Functional border colors
  static const Color dividerLight =
      Color(0xFFE0E0E0); // 1px functional separation
  static const Color dividerDark = Color(0xFF424242);

  // Text colors following athletic hierarchy
  static const Color textPrimaryLight = Color(0xFF212121); // High contrast
  static const Color textSecondaryLight =
      Color(0xFF757575); // Medium gray for supporting info
  static const Color textDisabledLight = Color(0xFFBDBDBD); // Disabled state

  static const Color textPrimaryDark = Color(0xFFFFFFFF);
  static const Color textSecondaryDark = Color(0xFFB0BEC5);
  static const Color textDisabledDark = Color(0xFF616161);

  /// Light theme implementing Purposeful Athletic Minimalism
  static ThemeData lightTheme = ThemeData(
    brightness: Brightness.light,
    colorScheme: ColorScheme(
      brightness: Brightness.light,
      primary: primaryLight,
      onPrimary: onPrimaryLight,
      primaryContainer: accentLight,
      onPrimaryContainer: primaryLight,
      secondary: secondaryLight,
      onSecondary: onSecondaryLight,
      secondaryContainer: secondaryLight.withValues(alpha: 0.1),
      onSecondaryContainer: secondaryLight,
      tertiary: warningLight,
      onTertiary: onPrimaryLight,
